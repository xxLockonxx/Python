# 정수형(int)
var1 = 1
var2 = 2
var3 = -3

print('var1 : ', var1)
print('var2 : ', var2)
print('var3 : ', var3)

# 실수형(float)
var4 = 3.14
var5 = 1.123456789

print('var4 : ', var4)
print('var5 : ', var5)
print('var4 type : ', type(var4))
print('var5 type : ', type(var5))

# 논리형(bool)
var6 = True
var7 = False

print('var 6 : ', var6)
print('var 7 : ', var7)
print('var 6 type : ', type(var6))

# 문자열(String)
var8 = "Hello"
var9 = '안녕'
print('var 8 : ', var8)
print('var 9 : ', var9)
print('var 8 type : ', type(var8))
